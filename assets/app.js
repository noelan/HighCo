// any CSS you import will output into a single css file (app.css in this case)
import "./styles/app.scss";

// start the Stimulus application
import "./bootstrap";

let chart = document.querySelector(".chart");
let donators = chart.dataset.donators;
let totalAmount = chart.dataset.totalAmount;

/* 
- en abscisse : les montants des dons
- en ordonnée : le nombre de personnes uniques ayant donné le montant en abscisse
*/

let ctx = document.getElementById("myChart");
let myChart = new Chart(ctx, {
  type: "bar",
  data: {
    labels: JSON.parse(totalAmount),
    datasets: [
      {
        label: "Total of donations",
        data: JSON.parse(donators),
        backgroundColor: [
          "rgba(255, 99, 132, 0.2)",
          "rgba(54, 162, 235, 0.2)",
          "rgba(255, 206, 86, 0.2)",
          "rgba(75, 192, 192, 0.2)",
          "rgba(153, 102, 255, 0.2)",
          "rgba(255, 159, 64, 0.2)",
        ],
        borderColor: [
          "rgba(255, 99, 132, 1)",
          "rgba(54, 162, 235, 1)",
          "rgba(255, 206, 86, 1)",
          "rgba(75, 192, 192, 1)",
          "rgba(153, 102, 255, 1)",
          "rgba(255, 159, 64, 1)",
        ],
        borderWidth: 1,
      },
    ],
  },
  options: {
    scales: {
      y: {
        beginAtZero: true,
      },
    },
  },
});
